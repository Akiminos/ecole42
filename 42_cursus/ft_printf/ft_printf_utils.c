#include "ft_printf.h"

int		is_conv(int c)
{
	if (c == 's' || c == 'd' || c == 'i' || c == 'c' || c == 'x' || c == 'X' ||
			c == 'u' || c == 'p')
		return (1);
	return (0);
}

int		is_flag(int c)
{
	if (c == '-' || c == '*' || c == '.' || ft_isdigit(c))
		return (1);
	return (0);
}
